CLUSTER_PROMPT = """\
You are organizing ideas from a Crazy Eights rapid ideation session.

Question / challenge:
{question}

Here are all {total_ideas} ideas generated by the team:
{ideas_block}

Group these ideas into thematic clusters. Each cluster should have:
- A short theme label (3-5 words)
- The list of ideas that belong to it

An idea should appear in exactly one cluster. Aim for 3-7 clusters.

Respond in JSON:
{{
  "clusters": [
    {{
      "theme": "short theme label",
      "ideas": ["idea text 1", "idea text 2"]
    }}
  ]
}}
"""

DOT_VOTE_PROMPT = """\
You are voting in a Crazy Eights dot-voting round.

Question / challenge:
{question}

Your role: {agent_name}
{system_prompt}

Here are the clustered ideas from the team:
{clusters_block}

You have EXACTLY 3 votes. Rules:
- Vote for the 3 ideas you believe have the most potential.
- You CANNOT vote for your own ideas. Your ideas were: {own_ideas_block}
- Spread your votes across different clusters if possible.
- For each vote, give a 1-sentence reason.

Respond in JSON:
{{
  "votes": [
    {{"idea": "exact idea text you are voting for", "reason": "why"}},
    {{"idea": "exact idea text you are voting for", "reason": "why"}},
    {{"idea": "exact idea text you are voting for", "reason": "why"}}
  ]
}}
"""

